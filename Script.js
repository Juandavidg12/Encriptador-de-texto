  // Función para copiar el texto al portapapeles
  function copiarTexto() {
    let texto = document.getElementById("texto");
  
    // Selecciona el texto del área de texto
    texto.select();
    texto.setSelectionRange(0, 99999); // Para dispositivos móviles
  
    // Copia el texto al portapapeles
    document.execCommand("copy");
  
    // Mostrar una alerta indicando que el texto fue copiado
    swal("¡Copiado!", "El texto ha sido copiado al portapapeles", "success");
  }
  
